// ── Get exercise by ID ─────────────────────────
export function getExerciseById(id) {
  return EXERCISES.find(ex => ex.id === id) || null;
}

// ── Get exercises by muscle group ─────────────
export function getExercisesByMuscle(muscleGroup) {
  return EXERCISES.filter(ex => ex.muscleGroup === muscleGroup);
}

// ── Get alternatives (same muscle, different id) ─
export function getAlternatives(exerciseId, limit = 5) {
  const ex = getExerciseById(exerciseId);
  if (!ex) return [];
  return EXERCISES.filter(e => e.muscleGroup === ex.muscleGroup && e.id !== exerciseId).slice(0, limit);
}

